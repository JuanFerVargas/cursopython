# Ejemplo de concatenacion o suma de valores

numero1_cadena='10'
print(f'Numero 1 en cadena:{numero1_cadena}')
numero2_cadena='20'
print(f'Numero 2 en cadena:{numero2_cadena}')

resultado=numero1_cadena+numero2_cadena
print(f'El resultado concatenado es:{resultado}')


#convertimos a tipo entero

resultado=int(numero1_cadena)+int(numero2_cadena)       
print(f'El resultado de la suma es:{resultado}')        