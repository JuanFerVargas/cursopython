# Reemplazar Subcadenas

cadena = 'Hola, mundo!'
nueva_subcadena = cadena.replace('mundo', 'a todos')
print(f'La cadena original es:{cadena} y la nueva cadena es:{nueva_subcadena}')

# Sustituir Hola por Adios

nueva_cadena = cadena.replace('Hola', 'Adios')
print(f'La cadena original es:{cadena} y la nueva cadena es:{nueva_cadena}')
