# Variables en Python


# Declaración e Iniciación de Variables

edad = 28           # Entero - int
altura = 1.65       # Decimal - float
pais = 'Colombia'   # String - str

# Acceder a una variable

print('Valores Iniciales:')
print('Edad:', edad)
print('Altura:', altura)
print('Pais:', pais)

# Modificar una variable

edad = 30           # Entero
altura = 1.68       # Decimal

print('Valores Modificados:')
print('Edad:', edad)
print('Altura:', altura)

# Tipo de Dato Dinamico

edad = 'Treinta'           # Entero
print('Edad:', edad)

# Si queremos acceder a una variable no declarada manda error
telefono = '3006379946'     # Entero
print('Telefono:', telefono)
