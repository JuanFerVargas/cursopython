# operadores de comparacion (Relacionales)
print("*** operadores de comparacion (Relacionales) ***")
a,b=7,5
print(f"Valor inicial a:{a}, b:{b}")

# Operadores Igualdad ==
resultado=(a==b)
print(f"Resultado de a==b:{resultado}")

# Operador Diferente !=
resultado=(a!=b)
print(f"Resultado de a!=b:{resultado}")

# Operador Mayor que >
resultado=(a>b)
print(f"Resultado de a>b:{resultado}")

# Operador Menor que <
resultado=(a<b)
print(f"Resultado de a<b:{resultado}")