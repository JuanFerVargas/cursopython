# Caractes especiales

# El caracter \n es un salto de linea.
print('Hola \nMundo')
# El caracter \t es un tabulador.
print('\t Python es genial ')
# El caracter \' permite comilla simple.
print('Juan\' "Perez')
# El caracter \" permite comilla dobles.
print("Carla \" Gomez")
# El caracter \\ permite diagonal invertida.
print('Caracter \\ diagonal invertida')
