# Manejo de Subcadenas

cadena = 'Hola, Mundo!'

# Obtenemos la subcadena [inicio:fin] fin + 1
subcadena = cadena[0:4]

print(f'La subcadena 1 de {cadena} es: {subcadena}')

# Obtenemos la subcadena [inicio:fin] fin + 1
subcadenamundo = cadena[6:11]

print(f'La subcadena 2 de {cadena} es: {subcadenamundo}')
