# Operadores Aritmeticos

a=10
b=3

# Suma
suma=a+b
print(f'Suma entre {a} + {b} es: {suma}')

# Resta
resta=a-b
print(f'Resta entre {a} - {b} es: {resta}')

# Multiplicacion
multiplicacion=a*b
print(f'Multiplicacion entre {a} * {b} es: {multiplicacion}')

# Division Retorna Floating Point
division=a/b
print(f'Division entre {a} / {b} es: {division:.2}')

# Division Entera
division_entera=a//b
print(f'Division Entera entre {a} // {b} es: {division_entera}')

# Modulo
modulo=a%b
print(f'Modulo entre {a} % {b} es: {modulo}')

# Exponente
exponente=a**b
print(f'Exponente entre {a} ** {b} es: {exponente}')