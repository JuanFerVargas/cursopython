# Operadores Asignacion Compuestos

# +=, -=, *=, /=
print('*** Operadores Asignacion Compuestos ***')

a,b=10,15
print(f'Valor Inicial a: {a}, b: {b}')

# Operadores compuesto de suma +=
a+=b
print(f'Valor de a += b: {a}')

# Operadores compuesto de resta -=
a =10  
a-=b
print(f'Valor de a -= b: {a}')

# Operadores compuesto de multiplicacion *=
a =10  
a*=b
print(f'Valor de a *= b: {a}')

# Operadores compuesto de division /=
a=10
a/=b
print(f'Valor de a /= b: {a:.2f}')